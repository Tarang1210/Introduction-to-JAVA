package Milestone1;

public class RelationalLogicalOperators {

	public static void main(String[] args) {
		
		int a =10; int b = 10;
		
		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a == b);
		System.out.println(a >= b);
		System.out.println(a <= b);
		
		System.out.println(a >= b && b>=a);
		System.out.println(a > b || a<b);

	}

}
